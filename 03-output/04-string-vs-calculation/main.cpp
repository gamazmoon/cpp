/*
 *  خروجی - رشته متنی (Literal) در مقابل محاسبه عددی (Calculation)
 *
 * این کد تفاوت اساسی در نحوه رفتار دستور 'cout' با داده ها را نشان می دهد:
 * ۱. متن داخل نقل قول ("8+10") به صورت خام چاپ می شود.
 * ۲. عبارت ریاضی بدون نقل قول (8+10) ابتدا محاسبه شده و سپس نتیجه آن چاپ می شود.
 */
#include <iostream>
using namespace std;

int main()
{
    cout << "\n 1----------- \n";
    // ۱. خروجی به عنوان رشته متنی (Literal) - نتیجه: 8+10
    cout << "8+10";

    cout << "\n 2----------- \n";
    // ۲. خروجی به عنوان محاسبه عددی (Calculation) - نتیجه: 18
    cout << 8+10;

    return 0;
}