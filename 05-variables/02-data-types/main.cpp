#include <iostream>
using namespace std;

int main()
{
    // Declare variables
    int num1;
    int num2;
    double num3;
    double num4;
    bool boolVal;
    char myChar;
    string message;

    // Assign values to variables
    num1 = 1;
    num2 = 2;
    num3 = 3.54;
    num4 = 4.12;
    boolVal = true; // boolVal = false;
    myChar = '@';
    message = "Hello World!";

    // Show the values of the variables
    cout << "num1 = " << num1 << '\n';
    cout << "num2 = " << num2 << '\n';
    cout << "num3 = " << num3 << '\n';
    cout << "num4 = " << num4 << '\n';
    cout << "boolVal = " << boolVal << '\n';
    cout << "myChar = " << myChar << '\n';
    cout << "message = " << message << '\n';
}