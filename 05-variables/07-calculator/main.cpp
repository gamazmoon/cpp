#include <iostream>
using namespace std;

/*
جا به جایی مقدار دو متغیر
*/

int main()
{
    int a = 20, b = 5, z;

    z = a * b;
    cout << "a*b is:" << z << endl;

    z = a / b;
    cout << "a/b is:" << z << endl;

    z = a + b;
    cout << "a+b is:" << z << endl;

    z = a - b;
    cout << "a-b is:" << z << endl;

    return 0;
}