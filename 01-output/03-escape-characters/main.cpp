#include <iostream>
using namespace std;

int main()
{
    cout << "\n 1----------- \n";
    // 1. \n (خط جدید - Newline)
    cout << "test1\ntest2";

    cout << "\n 2----------- \n";
    cout << "test1\ttest2";

    // 3. \" (نقل قول دوبل - Double Quote)
    cout << "\n 3----------- \n";
    cout << "test1\"test2";

    // 4. \\ (بک‌اسلش - Backslash)
    cout << "\n 4----------- \n";
    cout << "test1\\test2";

    return 0;
}